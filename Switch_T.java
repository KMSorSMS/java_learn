public class Switch_T {
public static void main(String[] args){
    //声明season
    String season = "Summer";
    //执行switch分支语句
    switch (season){
        case "Spring":
            System.out.println("It's Spring");
            break;
        case "Summer":
            System.out.println("It's Summer");
            break;
        case "Autumn":
            System.out.println("It's Autumn");
            break;
        case "Winter":
            System.out.println("It's Winter");
            break;
    }
}
}